package labeledObjects;

public class Simpleobject {
	
	public final int Label;
	public final double slope;
	public final double intercept;
	public final double ifprep;
	
	public Simpleobject(final int Label, final double slope, final double intercept, final double ifprep){
		
		this.Label = Label;
		this.slope = slope;
		this.intercept = intercept;
		this.ifprep = ifprep;
		
	}

}
